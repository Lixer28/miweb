print ("Escribe 2 números para saber su suma, resta, multiplicación y división")

num1 = int(input())
num2 = int(input())

suma = int(num1+num2)
resta = int(num1-num2)
multiplicacion = int(num1*num2)
division = float(num1/num2)

print ("El resultado de la suma es "+ str(suma))
print ("El resultado de la resta es "+ str(resta))
print ("El resultado de la multiplicación es "+ str(multiplicacion))
print ("El resultado de la división es "+ str(division))